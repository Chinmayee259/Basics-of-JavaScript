//Print all even numbers from 0 to 100
let i = 0;
while(i <= 100){
    if(i % 2 === 0){
        console.log(i);
    }
    i++;
}
console.log("All even numbers has been printed.");

//Print all odd numbers from 0 to 100
i = 0;
while(i <= 100){
    if(i % 2 !== 0){
        console.log(i);
    }
    i++;
}
console.log("All odd numbers has been printed.");